# It is also a type of list in python which stores more than one values in a single variable .
# in dictionary we can assign a key to a element , by using the key we can access the the  value associated with that particular key.
d = {"BA": "Bhagyshree Ambulkar", "DS": "Dhiraj Shembekar", "RI": "Rekha Israni"}
print(d["BA"])  # Printing  the value by using key name i.e., BA, DS etc..
print(d.keys())  # Printing  all keys present inside dictionary using Key() Method
print(list(d.values()))    #  Printing all values present inside Dictionary using Values() Method
